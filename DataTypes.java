package com.demojava;

public class DataTypes {
    public static void main(String[] args) {
        boolean flag=true;
        System.out.println("boolean "+ flag);

        long longvalue=98765432123l;
        System.out.println("longvalue is "+longvalue);

        float value=23.3f;
        System.out.println("float "+value);

        double value1=233.33d;
        System.out.println("double "+value1);

        char value2='a';
        System.out.println("value "+value2);

        byte value3=123;
        System.out.println("value "+value3);

        short value4=12;
        System.out.println("value "+value4);
    }
}
